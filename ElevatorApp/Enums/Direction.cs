namespace ElevatorApp.Enums
{
    /// <summary>
    /// Represents the direction of elevator movement.
    /// </summary>
    public enum Direction
    {
        /// <summary>
        /// Indicates the elevator is moving up.
        /// </summary>
        Up,
        /// <summary>
        /// Indicates the elevator is moving down.
        /// </summary>
        Down,
        /// <summary>
        /// Indicates the elevator is not moving.
        /// </summary>
        None
    }
}
